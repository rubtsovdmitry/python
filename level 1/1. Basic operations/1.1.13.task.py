a = int(input("%55s" % "Введите целое число \"a\": "))
b = int(input("%55s" % "Введите целое число \"b\": "))
print("%54s" % "Сумма чисел равна:", a + b)
print("%54s" % "Разница чисел равна:", a - b)
print("%54s" % "Произведение чисел равно:", a * b)
print("%54s" % "Частное от деления равно:", "%.2f" % (a / b))
print("%54s" % "Остаток от деления чисел:", a % b)
print("%54s" % "Результат возведения числа \"a\" в степень \"b\" равен:", a ** b)