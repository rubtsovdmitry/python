trying = 1
less = "искомое число меньше"
more = "искомое число больше"
while x != (answer := int(input("%136s" % "Введите целое число: "))):
    trying += 1
    print("%135s" % less) if answer > x else print("%135s" % more)

print("%135s" % f"Всего попыток {trying}")